public static void mergeSortInteger(Integer a[ ],int length)
{

Integer [] tmp = new Integer[a.length];

int runs;
for (runs=0; runs<TESTSIZE; runs+=length) {
long startTime=System.nanoTime();
MergeSortInteger(a, tmp,  runs,  runs+length - 1);
long endTime   = System.nanoTime();
totaltime += (endTime - startTime);
}
totaltime = totaltime/(TESTSIZE/length);
}

private static void MergeSortInteger(Integer a[ ], Integer tmp[ ], int left, int right)
{
if( left < right )
{
int center = (left + right) / 2;
MergeSortInteger(a, tmp, left, center);
MergeSortInteger(a, tmp, center + 1, right);
Merge(a, tmp, left, center + 1, right);
}
}

private static void Merge(Integer a[ ],Integer tmp[ ], int left, int right, int rightEnd )
{
int leftEnd = right - 1;
int k = left;
int num = rightEnd - left + 1;

while(left <= leftEnd && right <= rightEnd)
if(a[left] <= a[right])
tmp[k++] = a[left++];
else
tmp[k++] = a[right++];

while(left <= leftEnd)    // Copy rest of first half
tmp[k++] = a[left++];

while(right <= rightEnd)  // Copy rest of right half
tmp[k++] = a[right++];

// Copy tmp back
for(int i = 0; i < num; i++, rightEnd--)
a[rightEnd] = tmp[rightEnd];
}


