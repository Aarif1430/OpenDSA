   public static void quickSortMedianOfThree(int[] intArray,int length) {

    long startTime,endTime;

    int runs;
    for (runs=0; runs<TESTSIZE; runs+=length) {
        startTime=millis();
        recQuickSort(intArray, runs, runs+length - 1);
        endTime   = millis();
        totaltime =+ (endTime - startTime);
    }
    totaltime = totaltime/(TESTSIZE/length);
}



