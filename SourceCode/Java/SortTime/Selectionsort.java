// Same as selsort, but check if the swap is necessary
static void selsortcheck(int[] A,int length) {

    int runs;
    totaltime = 0;
for (runs=0; runs<TESTSIZE; runs+=length) {
  time1 = millis();
  for (int i=runs; i<runs+length-1; i++) { // Select i'th biggest record
    int bigindex = 0;                // Current biggest index
    for (int j=1+runs; j<runs+length-i; j++) // Find the max value
      if (A[j] > A[bigindex])        // Found something bigger
        bigindex = j;                // Remember bigger index
    if (bigindex != runs+length-i-1)
      swap(A, bigindex, runs+length-i-1); // Put it into place
  }
    time2 = millis();
    totaltime += time2-time1;
}
totaltime = totaltime/(TESTSIZE/length);

}

